fun main(){

    add(24,24)
    difference(78.0,23.0)
    multiply(23.0,5.0)
    divide(35.0,7.0)
}

fun add(num1:Double,num2:Double){
    println(num1+num2)
}

fun difference(num1:Double,num2:Double){
    println(num1-num2)
}

fun multiply(num1:Double,num2:Double){
    println(num1*num2)
}

fun divide(num1:Double,num2:Double){
    println(num1/num2)
}